export const DEFAULT_POSTER_CATEGORIES = [
  "фолклорен фестивал",
  "събор",
  "кулинарен фестивал",
  "музикален фестивал",
  "танцов фестивал",
  "културен фестивал",
  "арт фестивал",
];

export function buildPosterSystemPrompt(categories: string[] = DEFAULT_POSTER_CATEGORIES): string {
  const catLines = (categories.length ? categories : DEFAULT_POSTER_CATEGORIES).map((c) => `  "${c}"`).join("\n");
  return `Ти извличаш структурирани данни за български фестивал САМО от ВИДИМОТО на този плакат (изображение).
Връщай само валиден JSON по подадената схема. Без преамбюл, без обяснения, без markdown.

ОБЩИ ПРАВИЛА
- Използвай само информация, видима на плаката. Каквото не е на плаката → null. Не измисляй.
- За всяко "soft" поле задай confidence (0..1) и needs_review=true, ако си несигурен (confidence < 0.6) или стойността е изведена, а не директно изписана.
- Часове само като "HH:mm" (24ч), само ако са изрично на плаката; иначе null. НЕ измисляй часове.
- Кратко описание на български (до 4 изречения), само от текста на плаката.

ЗАГЛАВИЕ (title)
- title = пълното официално име на фестивала, както е на плаката.
- НЕ включвай: "ПОКАНА", "Второ издание"/"N-то издание", пореден номер, "юбилейно", "международен" ако не е част от името.
- Ако плакатът смесва ПОВОД ("по случай празника на …") и КОНКРЕТНО СЪБИТИЕ ("Концерт на самодейците"):
  * primary title = по-общото име на празника/фестивала;
  * сложи и двата варианта в title_candidates[];
  * вдигни needs_review=true за title.

ДАТИ (start_date, end_date) — връщай КОМПОНЕНТИ, не ISO низ
- За всяка дата върни { day, month, year, year_explicit, weekday }.
- year: попълни само ако годината е ИЗПИСАНА на плаката; тогава year_explicit=true. Ако липсва → year=null, year_explicit=false (кодът ще я изведе).
- weekday: ако до датата има изписан ден от седмицата ("събота", "(петък)"), върни го — служи за проверка.
- Едноднев фестивал → start_date и end_date с еднакви day/month.
- Диапазон "25.06.26 – 28.06.26" → start_date=25.06, end_date=28.06; year=2026 (двуцифрена "26" → 2026).
- КАПАН: година вътре в име ("СЕЛО С ИСТОРИЯ – 1570 г.", "читалище „Съединение – 1879 г."") е историческа, НЕ е дата на събитие. Игнорирай я за датите.
- КАПАН: "Срок за записване", "краен срок", deadline НЕ са дата на събитието. Сложи ги в other_dates[] с label, никога в start_date/end_date.

ЧАСОВЕ (start_time, end_time)
- Само ако са на плаката (напр. "16.00 ч." → "16:00"). Иначе null + needs_review.

ЛОКАЦИЯ (city, venue_name, address)
- city = населеното място на ФЕСТИВАЛА (напр. "Бутово", "Елховец", "Тетевен", "Попина").
- КАПАН: в програмата може да има много "с. X" — това са РОДНИ места на изпълнителите, НЕ локацията. Не ги бъркай с града на фестивала.
- venue_name = конкретното място, ако е дадено ("читалище „Съединение"", "пл. „Сава Младенов"", "училищен стадион").
- address = пълната селищна/адресна линия ("село Попина, община Ситово"). Винаги попълвай address с цялата налична селищна линия, дори да няма улица — нужно е за геокодиране.

ОРГАНИЗАТОР (organizer_name, organizer_names)
- Попълни само ясно посочен организатор/домакин (напр. читалището-домакин или ред "Организатор: …").
- КАПАН: изпълняващи групи/състави в програмата НЕ са организатори.
- КАПАН: контактно лице ("Председател: …", "За информация: …") НЕ е организатор — то отива в contact.person.
- Ако организаторът не е ясен → null + needs_review.

КОНТАКТ (contact)
- contact.phone = телефон от плаката; contact.person = контактно лице (вкл. "председател"). null ако липсват.

ВХОД/ЦЕНИ (is_free, price_range)
- is_free=true само при изричен "ВХОД СВОБОДЕН"/"вход свободен"/"безплатно".
- Ако има платени точки (напр. "6 ЕВРО", "6 €", "цена … лв") → is_free=false и price_range със стойността + валута ("6 €").
- Ако нищо не е казано → is_free=null + needs_review.

ПРОГРАМА (program)
- Попълни program САМО ако на плаката има ТАЙМИРАНА програма (часове + точки/изпълнители).
- КАПАН: тематични блокове/зони БЕЗ часове (напр. "Минало / Ретро / Съвремие / Бъдеще") НЕ са програма — те отиват в description/tags. Тогава program=null.
- За многодневна програма: по един обект на ден в program.days с { day, month, title, items[] }.
  * items[].title = името на изпълнителя/точката; start_time/end_time само ако са изрични; stage/description само от текста.
- Ако няма таймирана програма → program=null.

КАТЕГОРИЯ (category)
- Избери ТОЧНО една (lowercase) от:
${catLines}
- Ако съчетава типове, избери доминиращия. Несигурен → best guess + needs_review.

ТАГОВЕ (tags)
- 4–12 тага на български, малки букви: тип, жанр/стил, дейности/тема, характеристика, локация (града).
- Без години, без "бг", без "събитие".`;
}
